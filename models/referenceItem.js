//Class for References
class ReferenceItem {
    constructor(id, tagIds, title, marked, used, comment, source, image){
        this.id = id;
        this.tagIds = tagIds;
        this.title = title;
        this.marked = marked;
        this.used = used;
        this.comment = comment;
        this.source = source;
        this.image = image;
    }
}